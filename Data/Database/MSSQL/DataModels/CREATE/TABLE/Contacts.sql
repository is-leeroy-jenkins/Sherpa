IF NOT EXISTS ( SELECT * 
				FROM INFORMATION_SCHEMA.TABLES 
				WHERE TABLE_NAME = N'Contacts' )
BEGIN
CREATE TABLE [dbo].[Contacts](
	[ContactsId] [int] NOT NULL,
	[FirstName] VARCHAR(80) NULL DEFAULT ('NS'),
	[LastName] VARCHAR(80) NULL DEFAULT ('NS'),
	[FullName] VARCHAR(80) NULL DEFAULT ('NS'),
	[Email] VARCHAR(80) NULL DEFAULT ('NS'),
	[RPIO] VARCHAR(80) NULL DEFAULT ('NS'),
	[Section] VARCHAR(80) NULL
);
END
